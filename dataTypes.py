# None Data Type
# Nothing, or zero. No value.

# int and float
print(type(6))
print(type(2 - 4))
print(type(2 * 4))
print(type(2 / 4))

# power
print(2 ** 2)
print(2 ** 3)

# returns rounded down answer
print(2 // 4)

# returns remainder
print(6 % 4)